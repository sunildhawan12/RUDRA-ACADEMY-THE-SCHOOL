function convertToInputFormat(dateStr) {
  const parts = dateStr.split("/"); // DD/MM/YYYY
  if (parts.length !== 3) return "";
  const [dd, mm, yyyy] = parts;
  return `${yyyy}-${mm.padStart(2,'0')}-${dd.padStart(2,'0')}`;
}

